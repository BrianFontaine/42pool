char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += 32;
		i++;
	}
	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a' && str[i] <= 'z'))
		{
			if ((str[i - 1] >= 0 && str[i - 1] <= 47)
				|| (str[i - 1] >= 123 && str[i - 1] <= 127)
				|| (str[i - 1] >= 58 && str[i - 1] <= 64
				|| (str[i] >= 91 && str[i] <= 96)))
				str[i] -= 32;
		}
		i++;
	}
	return (str);
}
