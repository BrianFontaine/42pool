char	*ft_strncat(char *dest, char *src, int nb)
{
	int j;
	int i;

	j = 0;
	i = 0;
	while (dest[++j])
		while (i < nb && src[i])
		{
			dest[j] = src[i];
			i++;
			j++;
		}
	dest[j] = '\0';
	return (dest);
}
